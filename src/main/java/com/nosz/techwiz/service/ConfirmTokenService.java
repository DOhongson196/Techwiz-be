    @Transactional
    public String checkToken(String token){
        ConfirmToken confirmToken = confirmTokenRepository.findByToken(token)
                .orElseThrow(() -> new CustomException("Token not found"));
        if(confirmToken.getUser().getEnabled()){
            throw new CustomException("Email already confirmed");
        }

        LocalDateTime expireAt = confirmToken.getExpireAt();
        if(expireAt.isBefore(LocalDateTime.now())){
            throw new CustomException("Token expired");
        }
        userService.enableUser(confirmToken.getUser().getEmail());

        return "Confirmed";
    }

